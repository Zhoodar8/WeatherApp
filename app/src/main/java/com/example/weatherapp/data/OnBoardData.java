package com.example.weatherapp.data;

public class OnBoardData  {
    private String  txt;
    private int  img;

    public OnBoardData(String txt, int img) {
        this.txt = txt;
        this.img = img;
    }

    public String getTxt() {
        return txt;
    }

    public int getImg() {
        return img;
    }
}
